
public class Operator3 {
	public static void main(String[] args) {
		int value1 = 8;
		int value2 = 4;
		
		System.out.println(value1 > value2);
		
		System.out.println(value1 >= value2);
		
		System.out.println(value1 < value2);
		
		System.out.println(value1 <= value2);
		
		System.out.println(value1 == value2);
		
		System.out.println(value1 != value2);
	}
}
