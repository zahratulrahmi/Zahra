
public class Operator2 {
	public static void main(String[] args) {
		int value = 8;
		
		System.out.println(value++);
		System.out.println(value);
		System.out.println(++value);
		System.out.println(value);
		
		System.out.println(value--);
		System.out.println(value);
		System.out.println(--value);
		System.out.println(value);
		
	}
}
